def checkio(maze_map):
    #replace this for solution
    #This is just example for first maze
    visited = set()
    queue=[((1,1,),"" )]
    while queue:
        position, path = queue.pop(0)
        if position == (10,10):
            return path
        if position in visited:
            continue
        else:
            visited.add(position)
            for neigh_position, direction in get_neightbour(maze_map,position):
                if neigh_position in visited:
                    continue
                queue.append((neigh_position,path+direction))
    return False

def get_neightbour(maze,position):
    NEIGHBORS = {(-1, 0):'N', (0, 1):'E', (1, 0):'S', (0, -1):'W'}
    result=[]
    row,col = position
    for neighbor in NEIGHBORS:
        i, j = neighbor
        if 0 <= row + i < len(maze) and 0 <= col + j < len(maze[0]) and maze[row + i][col + j] == 0:
            result.append(((row+i,col+j),NEIGHBORS[(i,j)]))
    return result




